package com.bbyiya.model;

public class TiStylecoordinate {
    private Long styleid;

    private Integer imgcoordid;

    private Integer printcoordid;

    private Integer frontimgcoordid;

    public Long getStyleid() {
        return styleid;
    }

    public void setStyleid(Long styleid) {
        this.styleid = styleid;
    }

    public Integer getImgcoordid() {
        return imgcoordid;
    }

    public void setImgcoordid(Integer imgcoordid) {
        this.imgcoordid = imgcoordid;
    }

    public Integer getPrintcoordid() {
        return printcoordid;
    }

    public void setPrintcoordid(Integer printcoordid) {
        this.printcoordid = printcoordid;
    }

    public Integer getFrontimgcoordid() {
        return frontimgcoordid;
    }

    public void setFrontimgcoordid(Integer frontimgcoordid) {
        this.frontimgcoordid = frontimgcoordid;
    }
}